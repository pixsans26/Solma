// Helper to filter by genre
export function getByGenre(genre) {
  return ALL_CONTENT.filter((c) => c.genre.includes(genre));
}

// Helper to filter by category
export function getByCategory(category) {
  return ALL_CONTENT.filter((c) =>
    c.category.toLowerCase().replace(/\s+/g, '-') === category.toLowerCase().replace(/\s+/g, '-') ||
    c.category.toLowerCase() === category.toLowerCase()
  );
}

